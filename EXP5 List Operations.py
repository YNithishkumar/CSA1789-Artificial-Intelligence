# Nested List
nested_list = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]

# Length of List
list_len = len(nested_list)
print("Length of Nested List:", list_len)

# Concatenation of Lists
list1 = [1, 2, 3]
list2 = [4, 5, 6]
concat_list = list1 + list2
print("Concatenated List:", concat_list)

# Membership in List
list_mem = 6 in list2
print("Membership in List:", list_mem)

# Iteration over List
for item in list1:
  print("Item in List:", item)

# Indexing and Slicing of List
list_ind = list2[2]
print("Indexed Item in List:", list_ind)
list_sliced = list2[0:2]
print("Sliced List:", list_sliced)
